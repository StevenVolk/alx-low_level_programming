#include <stdio.h>
#include "dog.h"

/**
 * new_dog - store a copy of name and owner
 * @name: name of new_dog
 * @age: age of new_dog
 * @owner: owner of new_dog
 */
dog_t *new_dog(char *name, float age, char *owner)
{
	new_dog->name = name;
	new_dog->age = age;
	new_dog->owner = owner;
	if (new_dog)
		return (new_dog);
	else
		return (NULL);
}
