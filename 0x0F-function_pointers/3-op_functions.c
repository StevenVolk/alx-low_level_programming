#include <stdio.h>
#include "calc.h"

/**
 * op_add - sum of two interger
 * @a: first integer
 * @b: second integer
 * Return: sum of two integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

int op_sub(int a, int b)
{
	return (a - b);
}

int op_mul(int a, int b)
{
	return (a * b);
}

int op_div(int a, int b)
{
	return (a / b);
}

int op_mod(int a, int b)
{
	return (a % b);
}
